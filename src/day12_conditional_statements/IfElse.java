package day12_conditional_statements;

public class IfElse {
    public static void main(String[] args) {
        if(10 > 5) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }

        int count = 25;
        if (count > 30){
            System.out.println("Count is More than 30");
        } else {
            System.out.println("Count is Less than 30");
        }
    }
}
